function updateLanguage(lang) {
    try {
        const elements = document.querySelectorAll('[data-i18n]');
        elements.forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[lang] && translations[lang][key]) {
                if (element.tagName === 'INPUT' || element.tagName === 'TEXTAREA') {
                    element.placeholder = translations[lang][key];
                } else if (element.tagName === 'SELECT') {
                    const options = element.querySelectorAll('option');
                    if (key === 'program_interest_placeholder') {
                        if (options[0]) options[0].textContent = translations[lang][key];
                        if (options[1]) options[1].textContent = translations[lang]['it_option'];
                        if (options[2]) options[2].textContent = translations[lang]['design_option'];
                        if (options[3]) options[3].textContent = translations[lang]['business_option'];
                    } else if (key === 'campus_placeholder' || key === 'facility_placeholder') {
                        if (options[0]) options[0].textContent = translations[lang][key];
                        if (options[1]) options[1].textContent = translations[lang][key === 'campus_placeholder' ? 'campus_hanoi' : 'facility_hanoi'];
                        if (options[2]) options[2].textContent = translations[lang][key === 'campus_placeholder' ? 'campus_danang' : 'facility_danang'];
                        if (options[3]) options[3].textContent = translations[lang][key === 'campus_placeholder' ? 'campus_hcm' : 'facility_hcm'];
                        if (options[4]) options[4].textContent = translations[lang][key === 'campus_placeholder' ? 'campus_can_tho' : 'facility_can_tho'];
                    }
                } else {
                    element.textContent = translations[lang][key];
                }
            }
        });
        document.documentElement.lang = lang === 'vi' ? 'vi' : 'en';
        document.title = translations[lang].title || 'BTEC FPT - Student Portal';
    } catch (error) {
        console.error('Lỗi khi cập nhật ngôn ngữ:', error);
    }
}

function scrollToTrainingPrograms() {
    try {
        const trainingSection = document.querySelector('section[role="training-programs"]');
        if (trainingSection) {
            trainingSection.scrollIntoView({ behavior: 'smooth' });
        } else {
            console.error('Không tìm thấy phần Chương trình đào tạo');
        }
    } catch (error) {
        console.error('Lỗi khi cuộn đến Chương trình đào tạo:', error);
    }
}

function openZaloForm() {
    try {
        const modal = document.getElementById('zaloModal');
        if (modal) {
            modal.classList.remove('hidden');
        } else {
            console.error('Không tìm thấy modal Zalo');
        }
    } catch (error) {
        console.error('Lỗi khi mở form Zalo:', error);
    }
}

function closeZaloForm() {
    try {
        const modal = document.getElementById('zaloModal');
        if (modal) {
            modal.classList.add('hidden');
        } else {
            console.error('Không tìm thấy modal Zalo');
        }
    } catch (error) {
        console.error('Lỗi khi đóng form Zalo:', error);
    }
}

document.addEventListener('DOMContentLoaded', function () {
    try {
        const logo = document.getElementById('logo');
        if (logo) {
            logo.addEventListener('click', function (e) {
                const currentPath = window.location.pathname;
                if (currentPath.endsWith('index.php') || currentPath === '/') {
                    e.preventDefault();
                    window.location.reload();
                }
            });
        }

        const languageSelect = document.getElementById('language-select');
        if (languageSelect) {
            languageSelect.addEventListener('change', function () {
                const selectedLang = this.value;
                updateLanguage(selectedLang);
                localStorage.setItem('language', selectedLang);
            });

            const savedLanguage = localStorage.getItem('language') || 'en';
            languageSelect.value = savedLanguage;
            updateLanguage(savedLanguage);
        }

        const contactIcons = document.getElementById('contact-icons');
        if (contactIcons) {
            console.log('Tìm thấy icon liên hệ tại:', contactIcons.getBoundingClientRect());
            const style = window.getComputedStyle(contactIcons);
            console.log('Kiểu đã tính toán:', {
                position: style.position,
                bottom: style.bottom,
                right: style.right,
                zIndex: style.zIndex
            });
        } else {
            console.error('Không tìm thấy icon liên hệ trong DOM');
        }
    } catch (error) {
        console.error('Lỗi khi khởi tạo trang:', error);
    }
});

